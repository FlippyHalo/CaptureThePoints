package me.dalton.capturethepoints;

 /** A Spawn Point in a CTP arena */
public class Spawn {
    /** X co-ordinate of this Spawn */
    public double x;
    
    /** Y co-ordinate of this Spawn */
    public double y;
    
    /** Z co-ordinate of this Spawn */
    public double z;
    
    /** Direction players Spawn here */
    public double dir = 0.0D;
    
    /** The name of this Spawn. (i.e. the team color name) */
    public String name;
    
}
